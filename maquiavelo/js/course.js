  var scormReady = false;
  function initSCORM() {
    if (window.SCORM && SCORM.isAvailable()) {
      scormReady = SCORM.init();
      if (scormReady) {
        SCORM.set("cmi.core.lesson_status", "incomplete");
        SCORM.set("cmi.core.score.min", "0");
        SCORM.set("cmi.core.score.max", "100");
        SCORM.commit();
        // Restore bookmark
        try {
          var loc = SCORM.get("cmi.core.lesson_location");
          var t = parseInt(loc, 10);
          if (t > 0 && t < 7) {
            setTimeout(function () { goTo(t); }, 200);
          }
        } catch (e) {}
      }
    }
  }

  function commitProgress() {
    if (!scormReady) return;
    var now = Date.now();
    if (now - state.lastCommit < 1000) return;
    state.lastCommit = now;
    var pct = calcProgress();
    SCORM.set("cmi.core.score.raw", String(pct));
    SCORM.set("cmi.core.lesson_location", String(state.current));
    if (state.current === 8) {
      SCORM.set("cmi.core.lesson_status", state.quizCorrect >= 5 ? "passed" : "failed");
    } else {
      SCORM.set("cmi.core.lesson_status", "incomplete");
    }
    SCORM.commit();
  }

  function calcProgress() {
    var done = Object.keys(state.completed).length;
    var pct = Math.round((done / TOTAL_LESSONS) * 100);
    return Math.min(pct, 100);
  }

  function markCompleted(i) {
    state.completed[i] = true;
    var pct = calcProgress();
    var pctEl = document.getElementById("progressPct");
    if (pctEl) pctEl.textContent = pct + "%";
    var link = document.querySelector('.lesson-link[data-target="' + i + '"]');
    if (link) link.classList.add("completed");
  }

  // ====================================================
  // NAVEGACIÓN
  // ====================================================
  function goTo(i) {
    if (i < 0 || i >= TOTAL_LESSONS) return;
    // Mark previous (except quiz/completion) as completed when leaving
    if (state.current !== i && state.current !== 7 && state.current !== 8) {
      markCompleted(state.current);
    }
    state.current = i;

    document.querySelectorAll(".lesson").forEach(function (el) {
      el.classList.toggle("active", parseInt(el.getAttribute("data-lesson"), 10) === i);
    });
    document.querySelectorAll(".lesson-link").forEach(function (el) {
      el.classList.toggle("active", parseInt(el.getAttribute("data-target"), 10) === i);
    });
    var sc = document.getElementById("slideCount");
    if (sc) sc.textContent = (i + 1) + " de " + TOTAL_LESSONS;

    // Scroll to top
    var content = document.getElementById("content");
    if (content) content.scrollTop = 0;
    window.scrollTo(0, 0);

    // Stop any playing audio when changing lesson
    stopAllAudio();

    // Close sidebar on mobile
    var sidebar = document.getElementById("sidebar");
    if (sidebar) sidebar.classList.remove("open");

    commitProgress();
  }

