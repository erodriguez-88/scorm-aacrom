  // ====================================================
  // AUDIO PLAYER · TTS + try MP3 fallback
  // ====================================================
  var allAudio = []; // track all to stop
  function stopAllAudio() {
    allAudio.forEach(function (a) {
      if (a.howl) { try { a.howl.pause(); } catch (e) {} }
      if (a.utterance) { try { window.speechSynthesis.cancel(); } catch (e) {} }
      if (a.pill) a.pill.classList.remove("playing");
    });
  }

  document.querySelectorAll(".audio-pill").forEach(function (pill) {
    var key = pill.getAttribute("data-audio");
    var playBtn = pill.querySelector(".audio-play");
    var progressEl = pill.querySelector(".audio-progress");
    var dotEl = pill.querySelector(".audio-dot");
    var speedBtn = pill.querySelector(".audio-speed");

    var audioEntry = { pill: pill, howl: null, utterance: null };
    allAudio.push(audioEntry);

    var isPlaying = false;
    var nativeAudio = null;
    var speed = 1.0;

    // Try MP3 first
    function tryNativeAudio() {
      var src = "audio/" + key + ".mp3";
      var a = new Audio(src);
      a.preload = "auto";
      return new Promise(function (resolve, reject) {
        a.addEventListener("canplaythrough", function () { resolve(a); }, { once: true });
        a.addEventListener("error", function () { reject(); }, { once: true });
        a.load();
      });
    }

    function fallbackToTTS() {
      if (!('speechSynthesis' in window) || !NARRATION[key]) return null;
      var text = NARRATION[key].join(" ");
      var u = new SpeechSynthesisUtterance(text);
      u.lang = "es-ES";
      u.rate = 0.95;
      u.pitch = 1.0;
      return u;
    }

    playBtn.addEventListener("click", function () {
      if (isPlaying) {
        // Pause
        if (nativeAudio) nativeAudio.pause();
        else window.speechSynthesis.cancel();
        pill.classList.remove("playing");
        isPlaying = false;
        return;
      }

      // Stop any other audio
      allAudio.forEach(function (a) {
        if (a.pill !== pill) {
          if (a.howl) { try { a.howl.pause(); } catch (e) {} }
          a.pill.classList.remove("playing");
        }
      });
      try { window.speechSynthesis.cancel(); } catch (e) {}

      // Try native MP3 first
      tryNativeAudio().then(function (a) {
        nativeAudio = a;
        audioEntry.howl = a;
        a.playbackRate = speed;
        a.play();
        pill.classList.add("playing");
        isPlaying = true;

        a.addEventListener("timeupdate", function () {
          if (!a.duration) return;
          var p = (a.currentTime / a.duration) * 100;
          if (dotEl) dotEl.style.left = p + "%";
          var m = Math.floor(a.currentTime / 60);
          var s = Math.floor(a.currentTime % 60);
          if (progressEl) progressEl.textContent = m + ":" + (s < 10 ? "0" : "") + s;
        });
        a.addEventListener("ended", function () {
          pill.classList.remove("playing");
          isPlaying = false;
        });
      }).catch(function () {
        // Fallback to TTS
        var u = fallbackToTTS();
        if (!u) {
          alert("No se pudo reproducir el audio.");
          return;
        }
        audioEntry.utterance = u;
        u.rate = 0.95 * speed;
        u.onend = function () {
          pill.classList.remove("playing");
          isPlaying = false;
        };
        window.speechSynthesis.speak(u);
        pill.classList.add("playing");
        isPlaying = true;
      });
    });

    if (speedBtn) {
      speedBtn.addEventListener("click", function () {
        var speeds = [1.0, 1.25, 1.5, 0.75];
        var idx = speeds.indexOf(speed);
        speed = speeds[(idx + 1) % speeds.length];
        speedBtn.textContent = speed + "×";
        if (nativeAudio) nativeAudio.playbackRate = speed;
      });
    }
  });

